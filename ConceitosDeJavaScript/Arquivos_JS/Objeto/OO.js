//Procedural
processamento(valor1, valor2, valor3) //Uma função identificada por um nome que manipula dados que chega como parâmetro

//OO
objeto = { //Um objeto que possui valores e tem funções internamente. Então o objeto funciona como uma "capsula" que engloba comportamentos e valores
    valor1,
    valor2,
    valor3,
    processamento(){
        //...
    }
}
objeto.processamento() // O foco em OO passou a ser o objeto e antes em procedural era a função

